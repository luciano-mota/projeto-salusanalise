    private void salvarExamePaciente(){
        String sql = "insert into atendimento_paciente (nome_paciente,exame,observ,dtatendimento,dtentregaexame)values(?,?,?,?,?)";
   
        try {
            
            pst = conexao.prepareStatement(sql);
            
            //pegar o conteúdo dos campos 
            //esse numero, significa a ordem de inserção com os campos passados
            //na string
            
            //dados paciente
            pst.setString(1, txtNomePaciente.getText());
            
            
            
            //dados exames
            pst.setString(2,  txtAreaExame.getText());
           
            pst.setString(3, txtAreaObervacaoExame.getText());
            
            
            //dados exames
            
            pst.setString(4, txtDtAtendimentoExame.getText());
            
            pst.setString(5, txtDtEntregaExame.getText());
            
           
            //a linha abaixo atualiza a tabela usuario com os dados do formulario
            //confirmar se os dados foram salvos
            
            int add = pst.executeUpdate();
            
            if(add >0){
                JOptionPane.showMessageDialog(null, "Cadastro realizado!");
            }
            
        } catch (Exception e) {  
            JOptionPane.showMessageDialog(null, e);
        }
    
    }

        
    private void buscarNomePaciente(){
        
        //fazendo busca no banco passando o nome na hr da busca, por isso o like na consulta
        String sql = "select nome from paciente where nome like ?";
   
        try {
            
            pst = conexao.prepareStatement(sql);
           
            //desse jeito abaixo ele pega as informações passada no campo e concatena junto a string sql
            pst.setString(1, "%"+txtNomePaciente.getText()+"%");
            
           //depois de alimentar o ponto de interrogação
           
            rs = pst.executeQuery();
           
         
            if(rs.next()){
                
                // esse metodo next() diz se existe o proximo, ele vai pegar a informação do campo 1, que
                //é o campo nome, começa do 0 zero
                txtNomePaciente.setText(rs.getString(1));
   
            }else{
                //caso não encontre aparece a mensagem abaixo e limpa o campo
                //para não confundi o usuário do sistema
                
                JOptionPane.showMessageDialog(null, "Paciente não localizado!");
                txtNomePaciente.setText("");
            }
            
        } catch (Exception e) {  
            JOptionPane.showMessageDialog(null, e);
        }
    
    }
